    private void search() {
        String locId;
        String cusId;
        String vouNo;
        String fDate = Util1.toDateStr(txtFromDate.getDate(), "dd/MM/yyyy");
        String tDate = Util1.toDateStr(txtToDate.getDate(), "dd/MM/yyyy");
        if (locationAutoCompleter.getLocation() != null) {
            locId = locationAutoCompleter.getLocation().getLocationId().toString();
        } else {
            locId = "-";
        }
        if (traderAutoCompleter.getTrader() != null) {
            cusId = String.valueOf(traderAutoCompleter.getTrader().getId());

        } else {
            cusId = "-";
        }
        if (!txtVouNo.getText().equals("")) {
            vouNo = txtVouNo.getText();
        } else {
            vouNo = "-";
        }
        try {

            List listGl = retInService.search(fDate, tDate, cusId, locId, vouNo, "-", "5", "Ã„CCOUNT-RETIN", Global.compId.toString());
            retInVouSearchTableModel.setListGl(listGl);

        } catch (Exception ex) {
            LOGGER.error("Search RetInVouSearch.." + ex.getMessage());
        }

    }

