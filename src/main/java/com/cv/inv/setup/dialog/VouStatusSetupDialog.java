    private void initTable() {
        tblVouStatus.setModel(vouStatusTableModel);
        sorter = new TableRowSorter<>(tblVouStatus.getModel());
        tblVouStatus.setRowSorter(sorter);
        tblVouStatus.getTableHeader().setFont(Global.lableFont);
        tblVouStatus.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        tblVouStatus.setDefaultRenderer(Object.class, new TableCellRender());
        tblVouStatus.getSelectionModel().addListSelectionListener((ListSelectionEvent e) -> {
            if (e.getValueIsAdjusting()) {
                if (tblVouStatus.getSelectedRow() >= 0) {
                    selectRow = tblVouStatus.convertRowIndexToModel(tblVouStatus.getSelectedRow());
                    setVouStatus(vouStatusTableModel.getVouStatus(selectRow));
                }
            }
        });

    }

    private void setVouStatus(VouStatus vouStatus) {
        txtName.setText(vouStatus.getStatusDesp());
        lblStatus.setText("EDIT");
    }

    private boolean isValidEntry() {
        boolean status = true;

        if (txtName.getText().isEmpty()) {
            status = false;
            JOptionPane.showMessageDialog(this, "Code length is less then 6 character.",
                    "Code length", JOptionPane.ERROR_MESSAGE);
            txtName.requestFocusInWindow();
        } else {
            vouStatus = new VouStatus();
            vouStatus.setStatusDesp(txtName.getText());
        }

        return status;
    }

    private void save() {
        if (isValidEntry()) {
            VouStatus saveVou = vouService.save(vouStatus);
            if (saveVou != null) {
                LOGGER.info("VOUSTATUS ID :" + saveVou.getVouStatusId());
                JOptionPane.showMessageDialog(Global.parentForm, "Saved");

                if (lblStatus.getText().equals("NEW")) {
                    Global.listVou.add(saveVou);
                } else {
                    Global.listVou.set(selectRow, saveVou);
                }
                clear();
            }

        }
    }

    private void clear() {
        txtName.setText(null);
        vouStatusTableModel.refresh();
        lblStatus.setText("NEW");
    }

    private void searchVouStatus() {
        vouStatusTableModel.setListVouStatus(Global.listVou);
    }

    private void delete() {
        VouStatus vou = vouStatusTableModel.getVouStatus(selectRow);
        int delete = vouService.delete(vou.getVouStatusId().toString());
        if (delete == 1) {
            JOptionPane.showMessageDialog(Global.parentForm, "Deleted");
            vouStatusTableModel.deleteVouStatus(selectRow);
        } else {
            JOptionPane.showMessageDialog(Global.parentForm, "Error in server.");
        }
        clear();
    }

